package main.java.java.a.beginners.guide.chapter01;

public class GalToLit {
    public static void main(String[] args) {
        double gallons;
        double liters;

        gallons = 10;
        liters = gallons * 3.7854;

        System.out.println(gallons + " галлонам соответствует " + liters + " литра");
    }
}